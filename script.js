function getMakeDistribution(data) {
  const makeCount = {};
  data.forEach((vehicle) => {
    makeCount[vehicle.Make] = (makeCount[vehicle.Make] || 0) + 1;
  });
  return makeCount;
}

function getCountyDistribution(data) {
  const countyCount = {};
  data.forEach((vehicle) => {
    countyCount[vehicle.County] = (countyCount[vehicle.County] || 0) + 1;
  });
  return countyCount;
}

function getYearDistribution(data) {
  const yearCount = {};
  data.forEach((vehicle) => {
    yearCount[vehicle["Model Year"]] =
      (yearCount[vehicle["Model Year"]] || 0) + 1;
  });
  return yearCount;
}

